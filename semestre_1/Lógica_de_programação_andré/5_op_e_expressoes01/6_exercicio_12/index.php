<?php
// Definindo as variáveis iniciais
$numero1 = 10;
$numero2 = 5;

// Realizando as operações básicas
$soma = $numero1 + $numero2; // Soma
$multiplicacao = $soma * $numero2; // Multiplicação
$divisao = $multiplicacao / $numero1; // Divisão
$subtracao = $divisao - $numero2; // Subtração

// Imprimindo cada uma das etapas
echo "Resultado da soma: " . $soma . "\n";
echo "Resultado da multiplicação: " . $multiplicacao . "\n";
echo "Resultado da divisão: " . $divisao . "\n";
echo "Resultado da subtração: " . $subtracao . "\n";

?>

<?php
// Definindo as variáveis para a divisão exata
$dividendo1 = 10;
$divisor1 = 5;

// Realizando a operação de resto (divisão exata)
$restoExato = $dividendo1 % $divisor1;

// Imprimindo o resultado da divisão exata
echo "O resto da divisão de $dividendo1 por $divisor1 é: " . $restoExato . "\n";

// Definindo as variáveis para a divisão não exata
$dividendo2 = 10;
$divisor2 = 3;

// Realizando a operação de resto (divisão não exata)
$restoNaoExato = $dividendo2 % $divisor2;

// Imprimindo o resultado da divisão não exata
echo "O resto da divisão de $dividendo2 por $divisor2 é: " . $restoNaoExato . "\n";

?>
